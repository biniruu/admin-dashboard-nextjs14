import { Schema, model, models } from 'mongoose'

import { type Product as Products } from 'app/types'

const productSchema = new Schema<Products>(
  {
    title: {
      type: String,
      required: true,
      unique: true,
    },
    desc: {
      type: String,
      required: true,
    },
    price: {
      type: Number,
      required: true,
      min: 0,
    },
    stock: {
      type: Number,
      required: true,
      min: 0,
    },
    img: {
      type: String,
    },
    color: {
      type: String,
    },
    size: {
      type: String,
    },
  },
  { timestamps: true },
)

export const Product = models.Product || model<Products>('Product', productSchema)
